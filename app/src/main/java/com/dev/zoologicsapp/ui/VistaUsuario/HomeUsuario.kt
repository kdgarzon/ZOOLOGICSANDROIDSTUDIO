package com.dev.zoologicsapp.ui.VistaUsuario

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import android.widget.TextView
import androidx.fragment.app.Fragment
import androidx.lifecycle.ViewModelProvider
import com.dev.zoologicsapp.databinding.FragmentHomeUsuarioBinding

class HomeUsuario : Fragment() {

    private var _binding: FragmentHomeUsuarioBinding? = null

    // This property is only valid between onCreateView and
    // onDestroyView.
    private val binding get() = _binding!!

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        val homeUsuarioViewModel =
            ViewModelProvider(this)[HomeUsuarioViewModel::class.java]

        _binding = FragmentHomeUsuarioBinding.inflate(inflater, container, false)
        val root: View = binding.root

        val textView: TextView = binding.textHomeUsuario
        homeUsuarioViewModel.text.observe(viewLifecycleOwner) {
            textView.text = it
        }
        return root
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}